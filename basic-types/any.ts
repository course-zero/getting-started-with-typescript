// declare a variable of type `any`
let value: any = null;

// reassign with a `string` value
value = 'Hello World';
console.log( value );

// reassign with a `number` value
value = 1234;
console.log( value );

// reassign with a `boolean` value
value = true;
console.log( value );

// reassign with a `symbol` value
value = Symbol();
console.log( value );

// reassign with a `undefined` value
value = undefined;
console.log( value );

// reassign with a `null` value
value = null;
console.log( value );

// reassign with a `object` value
value = { a: 1 };
console.log( value );